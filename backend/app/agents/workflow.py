# ─── State 定义 ────────────────────────────────────────────────────────────────


class AgentRoleLiteral(str):
    CEO = "ceo"
    ARCHITECT = "architect"
    RESEARCHER = "researcher"
    WRITER = "writer"
    CODER = "coder"
    REVIEWER = "reviewer"
    TESTER = "tester"
    DEVOPS = "devops"
    PRODUCT_MANAGER = "pm"
    BRAINSTORMER = "brainstormer"


class AgentMessage(BaseModel):
    """Agent 之间传递的消息"""
    from_agent: str
    to_agent: str | None = None  # None = broadcast
    content: str
    attachments: list[str] = Field(default_factory=list)
    timestamp: datetime = Field(default_factory=datetime.utcnow)


class GraphState(TypedDict, total=False):
    """LangGraph 全局状态"""
    project_id: str
    task: str
    task_id: str
    current_agent: str
    next_agent: str | None
    results: dict
    errors: list[dict]
    iteration: int
    max_iterations: int
    messages: list[AgentMessage]
    context: dict
    output: str | None
    status: Literal["pending", "running", "completed", "failed"]


# ─── Agent 节点函数 ────────────────────────────────────────────────────────────


async def orchestrator_node(state: GraphState) -> dict:
    """任务分解节点 — CEO 角色（使用 MODEL_PRO）"""
    task = state.get("task", "")
    iteration = state.get("iteration", 0)

    logger.info(f"[orchestrator] 分解任务（模型={MODEL_PRO}）: {task[:50]}... (iter={iteration})")

    # 简单规则：根据关键词决定下一个 agent
    task_lower = task.lower()
    if any(k in task_lower for k in ["写", "code", "代码", "implement", "build"]):
        next_agent = AgentRoleLiteral.CODER
    elif any(k in task_lower for k in ["研究", "research", "搜索", "调研"]):
        next_agent = AgentRoleLiteral.RESEARCHER
    elif any(k in task_lower for k in ["写文章", "文档", "doc", "content"]):
        next_agent = AgentRoleLiteral.WRITER
    elif any(k in task_lower for k in ["review", "review", "审查", "检查"]):
        next_agent = AgentRoleLiteral.REVIEWER
    else:
        next_agent = AgentRoleLiteral.CODER  # 默认走 coder

    return {
        "current_agent": AgentRoleLiteral.CEO,
        "next_agent": next_agent,
        "iteration": iteration + 1,
    }


async def researcher_node(state: GraphState) -> dict:
    """研究节点 — 搜集信息"""
    task = state.get("task", "")
    logger.info(f"[researcher] 研究任务: {task[:50]}...")

    # 模拟研究结果
    result = {
        "findings": [
            f"研究结果1: 关于{task[:30]}的相关资料",
            f"研究结果2: 竞品分析资料",
            f"研究结果3: 技术选型建议",
        ],
        "summary": f"已完成对'{task}'的初步研究，共发现3个关键点。",
    }

    messages = state.get("messages", []) + [
        AgentMessage(from_agent=AgentRoleLiteral.RESEARCHER, content=result["summary"])
    ]

    return {
        "current_agent": AgentRoleLiteral.RESEARCHER,
        "next_agent": AgentRoleLiteral.CODER,
        "results": {**state.get("results", {}), "research": result},
        "messages": messages,
    }


